package ATB9x.oct21102024_forLoop;

public class Lab086 {
    public static void main(String[] args) {

        for(float f=0.0f;f<10.77;f++)
        {
            System.out.println(f);
        }
        for(long l=0;l<10;l++){
            System.out.println(l);
        }
        for (char c='a';c <'z';c++){
            System.out.println(c);
        }
    }
}
